// cargo is the official Rust package management tool. 
// It has lots of really useful features to improve code quality and developer velocity!
// or example, suppose that we want our program to have a great CLI. 
// You can find lots of great packages on crates.io (the official Rust package registry). 
// One popular choice is clap. As of this writing, 
// the most recent published version of clap is 2.27.1. 


// To add a dependency to our program,
//  we can simply add the following to our Cargo.toml under [dependencies]: clap = "2.27.1". And that's it! You can start using clap in your program.
 
// To build our project we can execute cargo build anywhere in the project directory
//  (including subdirectories!). We can also do cargo run to build and run.
//  Notice that these commands will resolve all dependencies, download crates if needed,
//  and build everything, including your crate.
//  (Note that it only rebuilds what it has not already built, similar to make).

